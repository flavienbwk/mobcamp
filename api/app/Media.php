<?php

namespace App;

use \Illuminate\Database\Eloquent\Model;

class Media extends Model
{
    protected $table = "media";
    public $timestamps = true;
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'id',
        'name',
        'type',
        'size',
        'downloadable',
        'hash',
        'uri'
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'created_at' => 'datetime',
        'updated_at' => 'datetime',
    ];
}
